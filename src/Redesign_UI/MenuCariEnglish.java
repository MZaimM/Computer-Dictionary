    public void insert(String key, String value) { //method untuk memasukkan kunci dan nilai
        Data item = new Data(key, value); //inisialisasi dengan objek data baru dengan parameter keydan value
        String kunci = item.getKey(); // inisisialisasi variabel dengan nilai item.getKey()
        int hashval = hashFunc(kunci); // inisialisasi hashval dengan pemanggilan method hashfunc
        while (hashArray[hashval] != null) { //looping jika hashArray tidak bernilai null
            ++hashval; //increment hashval
            hashval %= hashArray.length; // operasi modulo hashval dengan panjang data hasharray
        }
        hashArray[hashval] = item; //inisialisai tabel hashArray dengan data item

    }

    public int hashFunc(String key) { //method hashFunc dengan tipe pengembalian integer dan satu parameter bertipe String
        return (Math.abs(key.hashCode())) % hashArray.length;
        //mengembalikan nilai hasil operasi
        //fungsi .hashCode() berfungsi mengubah String menjadi kode hash dalam bentuk integer
        //Math.abs() sendiri berfungsi mengembalikan nilai mutlak dari kode hash
    }

    public boolean FindKey(String key) {
        int hashVal = hashFunc(key);
        while (hashArray[hashVal] != null) {
            if (hashArray[hashVal].getKey().equals(key)) {
                return true;
            }
            ++hashVal;
            hashVal %= hashArray.length;
        }
        return false;
    }

    public String getValue(String Key) { //method getvalue yang berfungsi mengambil value dari key
        int hashVal = hashFunc(Key);
        while (hashArray[hashVal] != null) {
            if (hashArray[hashVal].getKey().equals(Key)) {
                return hashArray[hashVal].getValue(); // mengambil value dari key yang ditemukan di tabel hasharray[hashval]
            }
            ++hashVal;
            hashVal %= hashArray.length;
        }
        return null;
    }

    public void displayLinearProbing() {
        System.out.println();
        for (int j = 0; j < hashArray.length; j++) {
            if (hashArray[j] != null) {
                System.out.println(" | " + j + "\t | " + hashArray[j].getKey() + "\t | " + hashArray[j].getValue() + "\t |");
            } else {
                System.out.println(" | " + j + "\t | -- | -- |");
            }
        }
        System.out.println("");
    }

